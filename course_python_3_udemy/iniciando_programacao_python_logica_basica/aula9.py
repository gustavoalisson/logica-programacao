# Introdução a operadores aritmétricos (matemática)

adicao = 10 + 10 
print('Adição ', adicao)

subtracao = 10 - 5 
print('Subtracao ', subtracao)

multiplicacao = 10 * 10
print('Multiplicacao ',multiplicacao)

divisao = 10 / 2.2 # float
print('Divisao ', divisao)

divisao_inteira = 10 // 2
print('Divisao inteira ', divisao_inteira)

exponenciacao = 2 ** 10
print('Exponenciacao ', exponenciacao)

modulo = 25  % 2 # resto da divisao
print('Modulo ', modulo)