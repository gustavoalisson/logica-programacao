# if / elif    /else

condicao1 = False
condicao2 = True
condicao3 = False
condicao4 = False

if condicao1:
    print('Código para a condição 1')
elif condicao2:
    print('Código para a condição 2')
elif condicao3:
    print('Código para a condição 3')
elif condicao4:
    print('Código para a condição 4')             
else:
    print('Nenhuma condição foi satisfeita.')
    
print('fora do if')    


