# Tipo de dado bool (boolean)
# Ao questionar algo em um programa
# Só existem duas respostas possíveis:
# sim (True) ou não (False).
# Existem vários operadores para "questionar".
# Dentro eles o ==, que é um operador lógico que
# questiona se um valor é igual ao outro
print(10 == 10) # True
print(10 == 11) # False
# type bool
print(type(True))
print(type(False))
print(type(10 == 10)) 
print(type(10 == 11))
